    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
       
        pkgModelo.clsDAOPersona obj;
        obj=new pkgModelo.clsDAOPersona();
        String de=JOptionPane.showInputDialog("INGRESE EL ODDIGO DE ESTUDIANTE");
        //txtId.setText(de); este metodo tambien sirve par buscar por cedula
         obj.setId(de);
         if (de.isEmpty()){
            JOptionPane.showMessageDialog(null, "INGRESE CODIGO", "ERROR", JOptionPane.ERROR_MESSAGE);
            
        }else{
         
         java.util.ArrayList resu = new java.util.ArrayList();
         resu=obj.consultar();
         if (resu.size() == 0){
            JOptionPane.showMessageDialog(null, "NO EXISTE");
         }else{
             
            txtNombre.setText(resu.get(0).toString());
             txtNombrelibro.setText(resu.get(1).toString());
            txtReferencia.setText(resu.get(2).toString());
            txtId.setText(de);
        }
         }
        
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        // TO add your handling code here:
        pkgModelo.clsDAOPersona obj;
        obj=new pkgModelo.clsDAOPersona();
        int fila=tblInfo.getSelectedRow();
        if (fila != -1){
            String codigo=tblInfo.getValueAt(fila, 0).toString();
            obj.setId(codigo);
            obj.borrar();
            listar();
        }
        else{

            JOptionPane.showMessageDialog(null, "SELECCIONE UNA COLUMNA PARA BORRAR", "ERROR", JOptionPane.ERROR_MESSAGE);
            
     }
    }//GEN-LAST:event_btnBorrarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        
        pkgModelo.clsDAOPersona obj;
        String de=JOptionPane.showInputDialog("INGRESE CODIGO");
        obj=new pkgModelo.clsDAOPersona();
        obj.setId(de);
        if (de.isEmpty()){
            JOptionPane.showMessageDialog(null, "INGRESE CODIGO", "ERROR", JOptionPane.ERROR_MESSAGE);
            
        }else{
        obj.setNombre(txtNombre.getText());
        obj.setRef(txtReferencia.getText());
        obj.setNomlibro(txtNombrelibro.getText());
        if(obj.modificar() == false){
         listar();
            JOptionPane.showMessageDialog(rootPane, "Se modificó");
            borrar();
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "No Se modificó");
        }
        }
    }//GEN-LAST:event_btnModificarActionPerformed

