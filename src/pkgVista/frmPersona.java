    private void btnAgregarListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarListarActionPerformed
        // TODO add your handling code here:
        if (txtId.getText().isEmpty()&&txtNombre.getText().isEmpty()&&txtNombrelibro.getText().isEmpty()&&txtReferencia.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null, "CAMPOS VACIOS", "ERROR", JOptionPane.ERROR_MESSAGE);   
        }else{
            if (txtId.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null, "INGRESE CODIGO", "ERROR", JOptionPane.ERROR_MESSAGE);   
        }else{
              if (txtNombre.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null, "INGRESE NOMBRE", "ERROR", JOptionPane.ERROR_MESSAGE);   
        }else{
                    if (txtReferencia.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null, "INGRESE LA REFERENCIA DEL LIBRO", "ERROR", JOptionPane.ERROR_MESSAGE);   
        }else{
                          if (txtNombrelibro.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null, "INGRESE NOMBRE DEL LIBRO", "ERROR", JOptionPane.ERROR_MESSAGE);   
        }else{
        pkgModelo.clsDAOPersona obj;
        obj=new pkgModelo.clsDAOPersona();
        obj.setId(txtId.getText());
        obj.setNombre(txtNombre.getText());
        obj.setRef(txtReferencia.getText());
        obj.setNomlibro(txtNombrelibro.getText());
        obj.setFecha(lblfecha.getText());
        
        if (obj.insertar() == true){
            JOptionPane.showMessageDialog(rootPane, "NO SE PUDO PRESTAR");
            
        }
        else{
            JOptionPane.showMessageDialog(rootPane, " PRESTADO");
            listar();
            borrar();
        }
            }
                    }
              }
            }
        }
        
    }//GEN-LAST:event_btnAgregarListarActionPerformed

