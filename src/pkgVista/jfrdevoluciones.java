    private void btnentregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnentregarActionPerformed
        // TODO add your handling code here:
        pkgModelo.clsDAOentregar obj;
        obj=new pkgModelo.clsDAOentregar();
        if (resibonombre.getText().isEmpty()&&txtresiboId.getText().isEmpty()&&txtresiboReferencia.getText().isEmpty()&&resibonombrelibro.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null, "CAMPOS VACIOS", "ERROR", JOptionPane.ERROR_MESSAGE);   
        }else{
        if(resibonombre.getText().isEmpty()) {
         JOptionPane.showMessageDialog(null, "INGRESE NOMBRE", "ERROR", JOptionPane.ERROR_MESSAGE);
          
              
          }else{
            if(txtresiboId.getText().isEmpty()) {
         JOptionPane.showMessageDialog(null, "INGRESE CODIGO", "ERROR", JOptionPane.ERROR_MESSAGE);
          
              
          }else{
                if(txtresiboReferencia.getText().isEmpty()) {
         JOptionPane.showMessageDialog(null, "INGRESE REFERENCIA DELLIBRO", "ERROR", JOptionPane.ERROR_MESSAGE);
          
              
          }else{
                    if(resibonombrelibro.getText().isEmpty()) {
         JOptionPane.showMessageDialog(null, "INGRESE NOMBRE DEL LIBRO", "ERROR", JOptionPane.ERROR_MESSAGE);
          
              
          }else{
        String devolvio;
        String nodevolvio;
        nodevolvio="NO";
        devolvio="SI";
        
        obj.setId(txtresiboId.getText());
        obj.setNombre(resibonombre.getText());
        obj.setRef(txtresiboReferencia.getText());
        obj.setNomlibro(resibonombrelibro.getText());
        obj.setFechaentrega(lblfecha.getText());
        obj.setHora(hora.getText());
        obj.setEntrego(devolvio);
        
        if (obj.insertar() == true){
            obj.setEntrego(nodevolvio);
            JOptionPane.showMessageDialog(rootPane, "NO SE DEVOLVIO *!");
                 
        }
        else{
            
            JOptionPane.showMessageDialog(rootPane, " GRACIAS POR DEVOLVER EL LIBRO !");
            
            listar();
            entrega();
        }
        }
        }
        }
        }
        }
    }//GEN-LAST:event_btnentregarActionPerformed

    private void btnconsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnconsultarActionPerformed
        // TODO add your handling code here:
        
         pkgModelo.clsDAOentregar obj;
        obj=new pkgModelo.clsDAOentregar();
        
        String de=JOptionPane.showInputDialog("INGRESE NUMERO CODIGO");
        //txtId.setText(de); este metodo tambien sirve par buscar por cedula
         obj.setId(de);
         if (de.isEmpty()){
        JOptionPane.showMessageDialog(null, "INGRESE CODIGO", "ERROR", JOptionPane.ERROR_MESSAGE);
            
        }else{
         java.util.ArrayList resu = new java.util.ArrayList();
         resu=obj.consultar();
         if (resu.size() == 0){
            JOptionPane.showMessageDialog(null, "NO EXISTE");
         }else{
             
            resibonombre.setText(resu.get(0).toString());
             resibonombrelibro.setText(resu.get(2).toString());
            txtresiboReferencia.setText(resu.get(1).toString());
            txtresiboId.setText(de);
        }
        
         }
    }//GEN-LAST:event_btnconsultarActionPerformed

