    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        
        
        
        
        pkgModelo.clsDAOentregar obj;
        obj=new pkgModelo.clsDAOentregar();
        int fila=tableinfo.getSelectedRow();
        if (fila != -1){
            String codigo=tableinfo.getValueAt(fila, 0).toString();
            obj.setId(codigo);
            obj.borrar();
            listar();
        }
        else{

            JOptionPane.showMessageDialog(null, "SELECCIONE UNA COLUMNA PARA BORRAR", "ERROR", JOptionPane.ERROR_MESSAGE);
            
     }
    }//GEN-LAST:event_btneliminarActionPerformed

